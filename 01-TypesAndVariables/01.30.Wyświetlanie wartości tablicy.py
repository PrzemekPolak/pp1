#Stworzenie tablicy
tablica = [12, 6, 4, 9, 3]

#Określenie ilości '*' w zależności od wartości tablicy 
poz0 = tablica[0] * '*'
poz1 = tablica[1] * '*'
poz2 = tablica[2] * '*'
poz3 = tablica[3] * '*'
poz4 = tablica[4] * '*'

#Pokazanie rezultatów
print(f'{tablica[0]}: {poz0}')
print(f'{tablica[1]}: {poz1}')
print(f'{tablica[2]}: {poz2}')
print(f'{tablica[3]}: {poz3}')
print(f'{tablica[4]}: {poz4}')